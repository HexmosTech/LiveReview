import React, { ButtonHTMLAttributes, InputHTMLAttributes, ReactNode, SelectHTMLAttributes, ElementType, ComponentPropsWithRef } from 'react';
import classNames from 'classnames';

// ===== BUTTON COMPONENTS =====
type ButtonVariant = 'primary' | 'secondary' | 'outline' | 'ghost' | 'danger';
type ButtonSize = 'sm' | 'md' | 'lg';

interface ButtonProps extends ButtonHTMLAttributes<HTMLButtonElement> {
  variant?: ButtonVariant;
  size?: ButtonSize;
  icon?: ReactNode;
  iconPosition?: 'left' | 'right';
  isLoading?: boolean;
  fullWidth?: boolean;
  href?: string;
  as?: ElementType;
  to?: string; // For React Router Link
}

export const Button: React.FC<ButtonProps> = ({
  children,
  variant = 'primary',
  size = 'md',
  icon,
  iconPosition = 'left',
  isLoading = false,
  fullWidth = false,
  className,
  disabled,
  href,
  as,
  to,
  ...props
}) => {
  const baseStyles = 'inline-flex items-center justify-center font-medium rounded-lg transition-colors focus:outline-none focus:ring-2 focus:ring-offset-2';
  
  const variantStyles = {
    primary: 'bg-blue-600 text-white hover:bg-blue-700 focus:ring-blue-500', // Primary Blue #3B82F6
    secondary: 'bg-slate-700 text-slate-100 hover:bg-slate-600 focus:ring-blue-400',
    outline: 'border border-slate-500 bg-transparent text-blue-300 hover:bg-slate-700 hover:text-blue-200 focus:ring-blue-400', // Dark Blue #1E40AF
    ghost: 'bg-transparent text-slate-300 hover:bg-slate-700 hover:text-slate-100 focus:ring-blue-400',
    danger: 'bg-red-600 text-white hover:bg-red-700 focus:ring-red-500',
  };
  
  const sizeStyles = {
    sm: 'text-xs px-3 py-2',
    md: 'text-sm px-4 py-2.5',
    lg: 'text-base px-5 py-3',
  };
  
  const loadingOrDisabled = isLoading || disabled;
  
  const buttonContent = (
    <>
      {isLoading && (
        <svg className="animate-spin -ml-1 mr-2 h-4 w-4 text-current" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
          <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"></circle>
          <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
        </svg>
      )}
      {icon && iconPosition === 'left' && !isLoading && <span className="mr-2">{icon}</span>}
      {children}
      {icon && iconPosition === 'right' && <span className="ml-2">{icon}</span>}
    </>
  );
  
  // Common props for all element types
  const commonProps = {
    className: classNames(
      baseStyles,
      variantStyles[variant],
      sizeStyles[size],
      fullWidth ? 'w-full' : '',
      loadingOrDisabled ? 'opacity-70 cursor-not-allowed' : '',
      className
    ),
    disabled: loadingOrDisabled,
    ...props
  };
  
  // If a custom component is provided through 'as' prop (e.g., Link from react-router-dom)
  if (as) {
    const Component = as;
    return (
      <Component 
        {...commonProps}
        to={to} // Pass 'to' prop for react-router Link
      >
        {buttonContent}
      </Component>
    );
  }
  
  // If href is provided, render an anchor tag
  if (href) {
    return (
      <a
        href={href}
        className={classNames(
          baseStyles,
          variantStyles[variant],
          sizeStyles[size],
          fullWidth ? 'w-full' : '',
          loadingOrDisabled ? 'opacity-70 cursor-not-allowed' : '',
          className
        )}
      >
        {buttonContent}
      </a>
    );
  }
  
  // Otherwise render a button
  return (
    <button
      {...commonProps}
    >
      {buttonContent}
    </button>
  );
};

// ===== CARD COMPONENT =====
interface CardProps {
  children: ReactNode;
  className?: string;
  title?: string;
  subtitle?: string;
  footer?: ReactNode;
  badge?: string;
  badgeColor?: string;
}

export const Card: React.FC<CardProps> = ({
  children,
  className,
  title,
  subtitle,
  footer,
  badge,
  badgeColor = 'bg-blue-100 text-blue-800',
}) => {
  return (
    <div className={classNames('bg-slate-800 rounded-xl shadow-md border border-slate-700 card-dark', className)}>
      {(title || subtitle || badge) && (
        <div className="flex justify-between items-center p-5 border-b border-slate-700">
          <div>
            {title && <h3 className="text-lg font-semibold text-slate-100">{title}</h3>}
            {subtitle && <p className="text-sm text-slate-300 mt-1">{subtitle}</p>}
          </div>
          {badge && (
            <span className={`text-xs font-medium px-2.5 py-1 rounded-full ${badgeColor}`}>
              {badge}
            </span>
          )}
        </div>
      )}
      <div className="p-5">{children}</div>
      {footer && (
        <div className="border-t border-slate-700 p-5 bg-slate-900 rounded-b-xl">
          {footer}
        </div>
      )}
    </div>
  );
};

// ===== INPUT COMPONENTS =====
interface InputProps extends InputHTMLAttributes<HTMLInputElement> {
  label?: string;
  error?: string;
  icon?: ReactNode;
  iconPosition?: 'left' | 'right';
  helperText?: string;
}

export const Input: React.FC<InputProps> = ({
  label,
  error,
  icon,
  iconPosition = 'left',
  helperText,
  className,
  id,
  ...props
}) => {
  const inputId = id || `input-${Math.random().toString(36).substr(2, 9)}`;
  
  return (
    <div className="w-full">
      {label && (
        <label htmlFor={inputId} className="block text-sm font-medium text-slate-300 mb-1">
          {label}
        </label>
      )}
      <div className="relative">
        {icon && iconPosition === 'left' && (
          <div className="absolute inset-y-0 left-0 pl-3 flex items-center pointer-events-none text-slate-400">
            {icon}
          </div>
        )}
        <input
          id={inputId}
          className={classNames(
            'w-full rounded-lg border bg-slate-700 text-white focus:ring-2 transition-all duration-200 outline-none px-4 py-2.5',
            error 
              ? 'border-red-500 focus:border-red-500 focus:ring-red-400'
              : 'border-slate-600 focus:border-blue-500 focus:ring-blue-400',
            icon && iconPosition === 'left' ? 'pl-10' : '',
            icon && iconPosition === 'right' ? 'pr-10' : '',
            className
          )}
          aria-invalid={error ? 'true' : 'false'}
          {...props}
        />
        {icon && iconPosition === 'right' && (
          <div className="absolute inset-y-0 right-0 pr-3 flex items-center pointer-events-none text-slate-400">
            {icon}
          </div>
        )}
      </div>
      {(error || helperText) && (
        <p className={classNames('mt-1 text-sm', error ? 'text-red-400' : 'text-slate-400')}>
          {error || helperText}
        </p>
      )}
    </div>
  );
};

